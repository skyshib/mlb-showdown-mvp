#!/usr/bin/env node
// Fit the authentic-scale price model: least squares from card mechanics
// (src/rules/pricing.js features) to the printed points of the 3,544 classic
// MLB Showdown cards, one model per group (hitters / SP / RP). Every fifth
// card is held out to report honest error before the final full-data refit.
// Writes src/data/priceModel.js.
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { decodeCardRows } from "../src/data/realCards.js";
import { CLASSIC_CARD_ROWS } from "../src/data/classicCards.js";
import { priceFeatures, priceGroup } from "../src/rules/pricing.js";

const HERE = dirname(fileURLToPath(import.meta.url));

function solve(A, b) {
  // Gaussian elimination with partial pivoting on the normal equations.
  const n = b.length;
  const m = A.map((row, i) => [...row, b[i]]);
  for (let col = 0; col < n; col += 1) {
    let pivot = col;
    for (let row = col + 1; row < n; row += 1) {
      if (Math.abs(m[row][col]) > Math.abs(m[pivot][col])) pivot = row;
    }
    [m[col], m[pivot]] = [m[pivot], m[col]];
    if (Math.abs(m[col][col]) < 1e-9) throw new Error(`singular at column ${col}`);
    for (let row = 0; row < n; row += 1) {
      if (row === col) continue;
      const factor = m[row][col] / m[col][col];
      for (let k = col; k <= n; k += 1) m[row][k] -= factor * m[col][k];
    }
  }
  return m.map((row, i) => row[n] / m[i][i]);
}

function fit(rows) {
  const keys = Object.keys(rows[0].features);
  const dim = keys.length + 1; // + intercept
  const XtX = Array.from({ length: dim }, () => Array(dim).fill(0));
  const Xty = Array(dim).fill(0);
  for (const { features, y } of rows) {
    const x = [1, ...keys.map((key) => features[key])];
    for (let i = 0; i < dim; i += 1) {
      Xty[i] += x[i] * y;
      for (let j = 0; j < dim; j += 1) XtX[i][j] += x[i] * x[j];
    }
  }
  const w = solve(XtX, Xty);
  return { intercept: w[0], weights: Object.fromEntries(keys.map((key, i) => [key, w[i + 1]])) };
}

function predict(model, features) {
  let total = model.intercept;
  for (const [key, weight] of Object.entries(model.weights)) total += weight * features[key];
  return total;
}

function report(model, rows) {
  const mean = rows.reduce((sum, { y }) => sum + y, 0) / rows.length;
  let absErr = 0, sqErr = 0, sqTot = 0;
  for (const { features, y } of rows) {
    const err = predict(model, features) - y;
    absErr += Math.abs(err);
    sqErr += err * err;
    sqTot += (y - mean) * (y - mean);
  }
  return { mae: absErr / rows.length, r2: 1 - sqErr / sqTot };
}

const cards = decodeCardRows(CLASSIC_CARD_ROWS);
const groups = { hitter: [], SP: [], RP: [] };
for (const card of cards) {
  groups[priceGroup(card)].push({ features: priceFeatures(card), y: card.points });
}

const model = {};
const stats = {};
for (const [name, rows] of Object.entries(groups)) {
  const train = rows.filter((_, i) => i % 5 !== 0);
  const holdout = rows.filter((_, i) => i % 5 === 0);
  const trial = fit(train);
  const inSample = report(trial, train);
  const outSample = report(trial, holdout);
  console.error(
    `${name}: n=${rows.length}  train MAE ${inSample.mae.toFixed(1)} R2 ${inSample.r2.toFixed(3)}  ` +
    `holdout MAE ${outSample.mae.toFixed(1)} R2 ${outSample.r2.toFixed(3)}`
  );
  model[name] = fit(rows); // final model uses every card
  stats[name] = { n: rows.length, holdoutMae: Number(outSample.mae.toFixed(1)), holdoutR2: Number(outSample.r2.toFixed(3)) };
}

const header = `// Authentic-scale price model: printed-point weights fit by least squares
// against the 3,544 classic MLB Showdown cards (2000-2005, authentic printed
// points). One model per group; feature definitions live in
// src/rules/pricing.js. Holdout (every 5th card): ${Object.entries(stats)
    .map(([name, s]) => `${name} MAE ${s.holdoutMae} R2 ${s.holdoutR2}`)
    .join(", ")}.
// Generated by scripts/fit-price-model.mjs; do not hand-edit.
`;
writeFileSync(
  join(HERE, "../src/data/priceModel.js"),
  `${header}export const PRICE_MODEL = ${JSON.stringify(model, null, 2)};\n`
);
console.error("wrote src/data/priceModel.js");
